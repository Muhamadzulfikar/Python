print("Program Kalkulator Sederhana")
nilai1=int(input("Nilai 1:"))
pilihan=input("Operator yang dipilih(+, -, *, /):")
nilai2=int(input("Nilai 2:"))
if pilihan=="+":
    print(nilai1+nilai2)
if pilihan=="-":
    print(nilai1-nilai2)
if pilihan=="*":
    print(nilai1*nilai2)
if pilihan=="/":
    print(nilai1/nilai2)